function mudarIcone1(icone) {
	if (icone == IconeTwitter)
		document.getElementById("IconeTwitter").src = "images/twitterlogo2.png";

	if (icone == IconeArtstation)
		document.getElementById("IconeArtstation").src = "images/artstationlogo2.png";

	if (icone == IconeInstagram)
		document.getElementById("IconeInstagram").src = "images/instagramlogo2.png";
}

